GLYPH_W = 5
GLYPH_H = 7


def glyph(ch: str):
    return _G.get(ch.upper(), _G[" "])


def text_width(text: str, scale: int, spacing: int = 1) -> int:
    n = len(text)
    return n * GLYPH_W * scale + max(0, n - 1) * spacing * scale


def text_pixels(text: str, scale: int, spacing: int = 1):
    """Yield (x, y) on-pixels (relative to top-left) for `text` rendered at `scale`."""
    cursor = 0
    for ch in text:
        rows = glyph(ch)
        for ry, row in enumerate(rows):
            for rx, bit in enumerate(row):
                if bit == "1" or bit == "#":
                    for dy in range(scale):
                        for dx in range(scale):
                            yield cursor + rx * scale + dx, ry * scale + dy
        cursor += (GLYPH_W + spacing) * scale
